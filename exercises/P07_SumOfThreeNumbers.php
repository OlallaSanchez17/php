<?php

class P07_SumOfThreeNumbers {
    public function main(): void {
        // Define three numbers
        $numA = 100;
        $numB = 200;
        $numC = 300;
        // Calculate the sum
        $numeros = $numA + $numB + $numC;

        // Output the result
        echo "The sum of the numbers is " . $numeros . "\n" ;
    }
}
